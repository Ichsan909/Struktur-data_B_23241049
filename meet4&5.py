# belajar aritmatika dasar
x = 11.5
y = 4

# penjumlahan +

hasil = x + y

print ("x + y =",hasil)

# pengurangan

hasil = x - y

print ("x - y =",hasil)

# perkalian
hasil = x * y
print ("x * y =",hasil)

# pembagian
hasil = x / y
print ("x / y =",hasil)

# perpangkatan exponent **
hasil = x ** y
print ("x ^ y =",hasil)

# modulus %  (MODULUS ADALAH SISA BAGI)
hasil = y % x
print ("x mod y =",hasil)

# floor division (pembulatan ke bawah)
hasil = x // y
print ("x // y =",hasil)

